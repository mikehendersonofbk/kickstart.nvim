-- Autocmd: open Neo-tree on startup if starting in a directory
vim.api.nvim_create_autocmd('VimEnter', {
  callback = function(data)
    -- Check if we opened Neovim with a directory argument
    if vim.fn.isdirectory(data.file) == 1 then
      require('neo-tree.command').execute {
        source = 'filesystem',
        position = 'current',
        reveal = true,
      }
    end
  end,
})

